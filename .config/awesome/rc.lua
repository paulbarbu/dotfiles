-- battery warning
-- created by bpdp

local function trim(s)
  return s:find'^%s*$' and '' or s:match'^%s*(.*%S)'
end

battery_widget = wibox.widget.textbox()

bat_err_shown = false

local function bat_check()

  --local f_capacity = assert(io.open("/sys/class/power_supply/BAT1/capacity", "r"))
--  local f_status = assert(io.open("/sys/class/power_supply/BAT1/status", "r"))

  local f_capacity = io.open("/sys/class/power_supply/BAT1/capacity", "r")
  local f_status = io.open("/sys/class/power_supply/BAT1/status", "r")

  if f_capacity == nil or f_status == nil then
    if bat_err_shown == false then
        --assert(nil, 'Cannot open either the capacity or the status of the battery')
        naughty.notify({ preset = naughty.config.presets.critical,
                     title = "Cannot open either the capacity or the status of the battery",
                     text = awesome.startup_errors })
        bat_err_shown = true
    end
    do return end
  end

  local bat_capacity = tonumber(f_capacity:read("*all"))
  local bat_status = trim(f_status:read("*all"))

  local bat_text = bat_capacity .. "%"

  if (bat_capacity <= 20 and bat_status == "Discharging") then
    naughty.notify({
        title = "Battery Warning"
      , text = "Battery low! " .. bat_text .. " left!"
      , fg = "#ff0000"
      , bg = "#deb887"
      , timeout = 120
      , position = "top_right"
      , ontop = true
    })

    bat_text = "<span foreground=\"red\">" .. bat_text .. "</span>"

    if bat_capacity <= 5 then
      awful.util.spawn_with_shell("systemctl suspend")
    end

  elseif bat_status == "Charging" then
    bat_text = "<span foreground=\"green\">" .. bat_text .. "</span>"
  end
  battery_widget:set_markup(bat_text)
end

battimer = timer({timeout = 60})
battimer:connect_signal("timeout", bat_check)
battimer:start()

-- end here for battery warning

-- CPU temperature check

cputemp_widget = wibox.widget.textbox()

local function cputemp_check()
  local f_temp = assert(io.open("/sys/class/thermal/thermal_zone0/temp", "r"))
  local cpu_temp = tonumber(f_temp:read("*all"))/1000

  local cputemp_text = cpu_temp .. "°C"

  if cpu_temp >= 60 then
    cputemp_text = "<span foreground=\"red\">" .. cputemp_text .. "</span>"
  end

  cputemp_widget:set_markup(cputemp_text)

end

cputemp = timer({timeout = 120})
cputemp:connect_signal("timeout", cputemp_check)
cputemp:start()


